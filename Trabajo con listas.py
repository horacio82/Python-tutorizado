"""En este vídeo vemos las listas. Las listas son similares a los array de otros lenguajes y son unas estructuras muy utilizadas a la hora de programar"""

#Creando una lista:
trabajadores = ["Ana","María","Antonio","Miguel","Juan"]
print(trabajadores)

#imprime la longitud de la lista:
print(len(trabajadores))

#imprime el elemento de tal posición:
print(trabajadores[2])

#imprime el último elemento de la lista:
print(trabajadores[-1])

#imprime los 3 primeros elementos de la lista:
print(trabajadores[0:3])

print(trabajadores[2:3])